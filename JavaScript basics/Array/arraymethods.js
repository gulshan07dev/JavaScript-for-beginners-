// array methods

// array length -> this method return length of the array
console.log(arr.length);

// push -> this method used for add element in last of the array
arr.push(5);
console.log(arr);

// pop -> this method used for remove element in last of the array
arr.pop();
console.log(arr);

// shift -> this method used for remove element in begin of the array
arr.shift();
console.log(arr);

// unshift -> this method used for add element in begin of the array
arr.unshift(1);
console.log(arr);

// splice -> this method used for add element in specific position of the array
arr.splice(3, 0, 10);
console.log(arr);

// slice -> this method used for return a section of the array
console.log(arr.slice(1, 2));

// reverse -> this method used for return reverse of the array
arr.reverse();
console.log(arr);

// sort -> this method used for retun sort(ascending order)
arr.sort();
console.log(arr);