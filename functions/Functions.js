// A Function is a block of code that perform a specific task and can be used
// throughout the program.

// There are two types of function
// 1. inbuilt function [ex- console.log()]
// 2. user defined function

// declare a function
function add(x, y) { // x, y --> parameter
    return x + y;
}

// calling a function
console.log(add(10, 30)); // 10, 30 --> arguments